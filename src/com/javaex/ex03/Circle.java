package com.javaex.ex03;



	public class Circle {
	private int rd;

	

	public Circle(int rd) {
		super();
		this.rd = rd;
	}




	public int getRd() {
		return rd;
	}


	public void setRd(int rd) {
		this.rd = rd;
	}


	@Override
	public String toString() {
		return "Circle [rd=" + rd + "]";
	}
	
	
	
	
	
	
	
	
	
	
}
